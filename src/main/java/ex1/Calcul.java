package ex1;

/**
 * Classe qui réalise des opérations arythmétiques
 * @author DIGINAMIC
 */
public class Calcul {

	/** Méthode qui retourne l'addition des parametres a et b 
	 * 
	 * @param a parametre a
	 * @param b parametre b
	 * @return int 
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/** Methode qui retourne la soustraction de a et b
	 * 
	 * @param a (parametre a)
	 * @param b (parametre b)
	 * @return
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
